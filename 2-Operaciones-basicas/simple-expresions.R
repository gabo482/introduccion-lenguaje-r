# Asignación de variables x y y

x <- 5
y <- 2

# Operaciones básicas

# Suma

resultado <- x + y
resultado

# Resta

resultado <- x - y
resultado

# Asignar valores a variables a y b
a <- 10
b <- 3



# multiplicacion

resultado <- x * y
resultado

# division

resultado <- x / y
resultado

# Potencia

resultado <- a^2

# Modulo

resultado <- a %% b
resultado


resultado <- a %/% b
resultado


